#1.	Написати програму, яка генерує послідовність з 10 випадкових чисел в діапазоні від 1 до 10 і обчислює їх середнє арифметичне.
загальна_вартість = float(input("Введіть загальну вартість товару: "))
кількість = int(input("Введіть кількість товару: "))
вартість_одиниці = загальна_вартість / кількість
print("Вартість одиниці товару:", вартість_одиниці)
